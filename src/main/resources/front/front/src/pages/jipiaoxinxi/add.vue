  export default {
    data() {
      return {
        id: '',
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          hangbanbianhao: '',
          hangkonggongsi: '',
          tupian: '',
          chufadi: '',
          mudedi: '',
          feixingfangshi: '',
          chufashijian: '',
